// ─── City Normalization ────────────────────────────────────────────────────────

export function normalizeCity(city: string): string {
  const normalized = city.toLowerCase().trim();

  const cityMap: Record<string, string> = {
    'bangalore': 'bangalore',
    'bengaluru': 'bangalore',
    'blr': 'bangalore',
    'mumbai': 'mumbai',
    'bombay': 'mumbai',
    'mum': 'mumbai',
    'delhi': 'delhi',
    'new delhi': 'delhi',
    'ncr': 'delhi',
    'london': 'london',
    'new york': 'new york',
    'nyc': 'new york',
    'new york city': 'new york',
  };

  return cityMap[normalized] || normalized;
}

export function getCityActivities(city: string): Activity[] {
  const normalized = normalizeCity(city);
  return CITY_ACTIVITIES[normalized] || GENERIC_ACTIVITIES;
}

export function getCityFood(city: string): FoodOption[] {
  const normalized = normalizeCity(city);
  return CITY_FOOD[normalized] || GENERIC_FOOD;
}

export function getCurrencySymbol(city: string): string {
  const normalized = normalizeCity(city);
  const currencies: Record<string, string> = {
    bangalore: '₹',
    mumbai: '₹',
    delhi: '₹',
    london: '£',
    'new york': '$',
  };
  return currencies[normalized] || '₹';
}
